package entity;

public class EmployeeEntity {

  private String no;
  private String name;
  private String age;
  
  public String getNo(){
    return no;
  }
  public void setNo(String no){
    this.no = no;
  }
  public String getName(){
    return name;
  }
  public void setName(String name){
    this.name = name;
  }
  public String getAge(){
    return age;
  }
  public void setAge(String age){
    this.age = age;
  }
}